    private MainScreen mainScreen;
    private HighScorePanel highScorePanel;
    private CreditsScreen creditsScreen;
    /**
     * Creates new form MainFrame
     */
    
    // Method: MainFrame
    // Purpose: This is the method for the Main Frame that will handle initializing all
    // panels that will be used, as well as starting up all of the desired frame settings.
    // Most importantly, this also sets up the listeners for the panels attached to it.
    
    public MainFrame() {
        super("Hangman");
        setLayout(new FlowLayout());
        highScorePanel = new HighScorePanel();
        mainScreen = new MainScreen();
        creditsScreen = new CreditsScreen();
        setContentPane(mainScreen);
        setSize(600,400);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(EXIT_ON_CLOSE);
        setVisible(true);
        highScorePanel.setButtonListener(new ButtonListener(){
            public void btnClicked(String name){
                if (name.equals("Back")){
                    setContentPane(mainScreen);
                    invalidate();
                    validate();
                }
            }
        });
        mainScreen.setButtonListener(new ButtonListener(){
            public void btnClicked(String name){
                if (name.equals("Play")){
                    startScreen = new GameScreen();
                    dispose();
                }
                else if (name.equals("High")){
                    setContentPane(highScorePanel);
                    invalidate();
                    validate();
                }
                else if (name.equals("Credit")){
                    setContentPane(creditsScreen);
                    invalidate();
                    validate();
                }
            }
        });
        creditsScreen.setButtonListener(new ButtonListener(){
            public void btnClicked(String name){
                if (name.equals("Back")){
                    setContentPane(mainScreen);
                    invalidate();
                    validate();
                }
            }
        });
    }

